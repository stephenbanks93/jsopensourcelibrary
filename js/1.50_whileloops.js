//While Loops//

console.log("While loop");
var i = 1;

while (i <= 5){		//While "i" is less than or equal to "5",
	console.log(i); //console log the current value of "i" 
	i++;            //Adds 1 to the current value of "i"
};                    
//Keeps looping until the value of "i" is equal to "5" then stops

//Do While Loops//

console.log("Do While loop")
var x = 1;

do {
	console.log(x);
	x++;
} while (x <= 5);
